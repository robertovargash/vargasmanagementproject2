    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Proveedor  $proveedor
     * @return \Illuminate\Http\Response
     */
    public function show(Proveedor $proveedor)
    {
        $title = "Datos del proveedor";
        foreach ($proveedor->proveedorcuentas as $key => $cuenta) {
            $cuenta->qr = "TRANSFERMOVIL_ETECSA,TRANSFERENCIA,".$cuenta->cuenta.",".$proveedor->telefono;
        }
        return view('proveedors.show',compact('proveedor','title'));
    }

